function getLocale(): 'zh' | 'en' {
  try {
    if (typeof navigator !== 'undefined') {
      const lang = (navigator.language || '').toLowerCase();
      if (lang.startsWith('zh')) return 'zh';
      return 'en';
    }
  } catch {}
  return 'zh';
}

